inline void solution() {
  const int n = read::Int();
  const int k = read::Int();

  vector<int> skill = read::Vec<int>(n);
  sort(skill.begin(), skill.end());

  vector<vector<int>> best(n, vector<int>(k + 1));
  for (int groups = 1; groups <= k; groups++) {
    best[0][groups] = 1;
  }
  for (int i = 1; i < n; i++) {
    for (int groups = 1; groups <= k; groups++) {
      const int j = distance(skill.begin(), lower_bound(skill.begin(), skill.end(), skill[i] - 5));
      if (j == 0) {
        best[i][groups] = i + 1;
      } else {
        best[i][groups] = max(best[i - 1][groups], (i - j + 1) + best[j - 1][groups - 1]);
      }
    }
  }

  cout << best[n - 1][k] << '\n';
}

