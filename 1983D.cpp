template<class node, class F = std::function<node(const node &, const node &)>>
class SegTree {
  int n = 0;
  std::vector<node> t;
  F unite;

  void build(const int x, const int l, const int r, const std::vector<node> &a) {
    if (l == r) {
      t[x] = a[l];
      return;
    }
    const int mid = (l + r) / 2;
    const int y = 2 * (mid - l + 1) + x;
    build(x + 1, l, mid, a);
    build(y, mid + 1, r, a);
    t[x] = unite(t[x + 1], t[y]);
  }

  void update(const int x, const int l, const int r, const int p, const node &v) {
    if (l == p and p == r) {
      t[x] = v;
      return;
    }
    const int mid = (l + r) / 2;
    const int y = 2 * (mid - l + 1) + x;
    if (p <= mid) {
      update(x + 1, l, mid, p, v);
    } else {
      update(y, mid + 1, r, p, v);
    }
    t[x] = unite(t[x + 1], t[y]);
  }

  node query(const int x, const int l, const int r, const int ql, const int qr) const {
    if (ql <= l and r <= qr) {
      return t[x];
    }
    const int mid = (l + r) / 2;
    const int y = 2 * (mid - l + 1) + x;
    if (qr <= mid) {
      return query(x + 1, l, mid, ql, qr);
    } else if (mid < ql) {
      return query(y, mid + 1, r, ql, qr);
    } else {
      return unite(query(x + 1, l, mid, ql, qr), query(y, mid + 1, r, ql, qr));
    }
  }

public:
  SegTree() = default;
  explicit SegTree(const int n, const node e, F f) : n(n), t(2 * n - 1, e), unite(std::move(f)) {}
  explicit SegTree(const std::vector<node> &a, F f) : n(a.size()), t(2 * (a.size()) - 1), unite(std::move(f)) {
    build(0, 0, n - 1, a);
  }

  void update(const int p, const node &v) {
    assert(0 <= p and p < n);
    update(0, 0, n - 1, p, v);
  }

  [[nodiscard]] node query(const int l, const int r) const {
    assert(0 <= l and l <= r and r < n);
    return query(0, 0, n - 1, l, r);
  }
};

vector<int> freq(kMaxN);
inline void solution() {
  const int n = read<int>();
  auto a = read_vec<int>(n);
  auto b = read_vec<int>(n);

  if (a == b) {
    cout << "YES\n";
    return;
  }

  for (const int x : a) freq[x]++;
  for (const int x : b) freq[x]--;

  int mx = 0;
  for (const int x : a) mx = max(mx, freq[x]);

  for (const int x : a) freq[x] = 0;
  for (const int x : b) freq[x] = 0;

  if (mx != 0) {
    cout << "NO\n";
    return;
  }

  for (int i = 0; i < n; i++) {
    freq[b[i]] = i;
  }

  vector<int> foo(n, 1);
  SegTree<int> st(foo, [](const int x, const int y) {
    return x + y;
  });

  for (int i = 0; i < n - 2; i++) {
    const int steps = st.query(0, freq[a[i]]) - 1;
    st.update(freq[a[i]], 0);
    if (steps % 2) swap(a[n - 2], a[n - 1]);
  }

  cout << (freq[a[n - 2]] < freq[a[n - 1]] ? "YES" : "NO") << '\n';

  for (const int x : a) freq[x] = 0;
}

