void solve() {
  int n;
  cin >> n;
  vector<int> a(n);
  for (int &x : a) cin >> x;
  int cnt = 1, ans = 1;
  for (int i = 1; i < n; i++) {
    cnt = (a[i - 1] <= a[i] ? cnt + 1 : 1);
    ans = max(ans, cnt);
  }
  cout << ans << '\n';
}

