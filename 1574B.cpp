void solve(const int TestCase) {
  vector<int> a(3);
  int m;
  cin >> a[0] >> a[1] >> a[2] >> m;
  sort(a.rbegin(), a.rend());
  const int mn = max(0, a[0] - (a[1] + a[2]) - 1);
  const int mx = (a[0] - 1) + (a[1] - 1) + (a[2] - 1);
  cout << (mn <= m and m <= mx ? "YES" : "NO") << '\n';
}

